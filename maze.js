// テーブル要素取得
const table = document.getElementById("mazeTable");

// プレイヤー情報
let player = { x: 0, y: 0, hp: 222 };

// 通過済みマスの記録
let visited = Array(maze.length).fill().map(() => Array(maze[0].length).fill(false));

// 移動処理
document.addEventListener("keydown", (event) => {
    if (!["ArrowUp", "ArrowDown", "ArrowLeft", "ArrowRight"].includes(event.key)) return;

    let newX = player.x;
    let newY = player.y;

    if (event.key === "ArrowUp") newX--;
    if (event.key === "ArrowDown") newX++;
    if (event.key === "ArrowLeft") newY--;
    if (event.key === "ArrowRight") newY++;

    // 壁判定
    if (newX >= 0 && newX < maze.length && newY >= 0 && newY < maze[0].length && maze[newX][newY] !== 1) {
        visited[player.x][player.y] = true;

        // 位置とHP更新
        player.x = newX;
        player.y = newY;
        player.hp--;

        document.getElementById("hp").textContent = `体力: ${player.hp}`;

        // ゴール処理
        if (maze[player.x][player.y] === 2) {
            alert("くそッ…捕まった…爆弾のコードを切る順番は「青･赤･赤･青･赤」だ…");

            localStorage.setItem("reportMessage", "「コードを切る順番は…青赤赤青赤…」");

            document.body.classList.add("fade-out");
            setTimeout(() => {
                window.location.href = "index.html";
            }, 1000);
            return;
        }

        // 体力0判定
        if (player.hp <= 0) {
            alert("体力がなくなった… 爆弾魔に逃げられた…！ ↺再チャレンジ!");
            location.reload();
            return;
        }

        // 再描画
        renderMaze();
    }
});

// 迷路描画
function renderMaze() {
    table.innerHTML = "";
    for (let i = 0; i < maze.length; i++) {
        let row = table.insertRow();
        for (let j = 0; j < maze[i].length; j++) {
            let cell = row.insertCell();

            if (i === player.x && j === player.y) {
                cell.textContent = "🕵️‍♂️";
                cell.classList.add("player");
            } else if (maze[i][j] === 1) {
                cell.textContent = " ";
                cell.classList.add("wall");
            } else if (maze[i][j] === 2) {
                cell.textContent = "🦹";
            } else {
                cell.textContent = " ";
            }

            if (visited[i][j]) {
                cell.classList.add("passed");
            }
        }
    }
}

// 迷路の初期描画
renderMaze();